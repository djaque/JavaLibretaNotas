package beans;

/**
 *
 * @author dany
 */
public class NotasDto {
    
    private int id;
    private String name;
    private String text;
    private String date;

    public NotasDto() {
    }

    public NotasDto(String name, String text) {
        this.name = name;
        this.text = text;

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    
}
